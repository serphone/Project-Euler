def greatest_consecutive_product(number, digits):
    n = str(number)
    maxProduct = 0

    for i in range(len(n)-digits+1):
        prod = 1
        for j in range(digits):
            prod *= int(n[i+j])
        if prod > maxProduct:
            maxProduct = prod

    return maxProduct

